import UIKit

struct ColorThemeHelper {
    static func forBackground() -> UIColor {
        return UIColor(hex: 0x3A3A37)!
    }
    
    static func forFont() -> UIColor {
        return UIColor(hex: 0xE1E0D9)!
    }
}
